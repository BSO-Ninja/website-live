<script>

  var imbuementsJSON = [];

  $(document).ready(function() {

    $.getJSON('./json/imbuements.json', function(json) {
      imbuementsJSON = json;
    });

    $(".load_imbue").click(function(){
      var imbuement = $(this).attr('imbuement');
      var imbue = imbuementsJSON.imbuements[imbuement];

      $(".load_imbue").removeClass("load_imbue_active");
      $(this).addClass("load_imbue_active");

      $("#imbue_display_name").text(imbue.display_name);
      $("#imbue_icon").attr("src","/public/images/tibia/imbuements/"+ imbue.image);
      $("#imbue_slots_description").text(imbue.imbue_slots.description);
      $(".imbue_name").text(imbue.name);

      /* TIERS */
      $(".imbue_tier1_display_name").text(imbue.astral_sources.tier_1.display_name);
      $(".imbue_tier2_display_name").text(imbue.astral_sources.tier_2.display_name);
      $(".imbue_tier3_display_name").text(imbue.astral_sources.tier_3.display_name);
      $(".imbue_tier1_amount").text(imbue.astral_sources.tier_1.amount);
      $(".imbue_tier2_amount").text(imbue.astral_sources.tier_2.amount);
      $(".imbue_tier3_amount").text(imbue.astral_sources.tier_3.amount);
      $(".imbue_tier1_image").attr("src","/public/images/tibia/imbuements/"+ imbue.astral_sources.tier_1.image);
      $(".imbue_tier2_image").attr("src","/public/images/tibia/imbuements/"+ imbue.astral_sources.tier_2.image);
      $(".imbue_tier3_image").attr("src","/public/images/tibia/imbuements/"+ imbue.astral_sources.tier_3.image);
      $(".imbue_tier1_name").attr("itemname",imbue.astral_sources.tier_1.name);
      $(".imbue_tier2_name").attr("itemname",imbue.astral_sources.tier_2.name);
      $(".imbue_tier3_name").attr("itemname",imbue.astral_sources.tier_3.name);
      $(".imbue_tier1_effect").text(imbue.astral_sources.tier_1.effect);
      $(".imbue_tier2_effect").text(imbue.astral_sources.tier_2.effect);
      $(".imbue_tier3_effect").text(imbue.astral_sources.tier_3.effect);
      if(imbue.important_notes !== "") {
        $("#imbue_important_notes").text(imbue.important_notes);
      }
      else {
        $("#imbue_important_notes").text("");
      }
    });

  });

  $(".copyItemName").click(function(){
    var $temp = $("<input>");
    $("body").append($temp);
    $temp.val($(this).attr('itemname')).select();
    document.execCommand("copy");
    $(this).notify(
      $(this).attr('itemname') +" copied",
      {
        position: "right",
        className: 'success',
        showDuration: 100
      }
    );
    $temp.remove();
  });
</script>