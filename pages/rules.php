<h1 style="margin-left:10px;">TibiaMate Community Rules</h1>

<div class="box">
  <div class="content2">
    <h3>Supportive</h3>
    We encourage our members to give and receive support, sharing their stories and experiences in times of distress and wellness.
  </div>
</div>

<div class="box">
  <div class="content2">
    <h3>Respectful</h3>
    We respect where members are on their recovery journey, and listen without judgment.  Appreciate that others may have an opinion different from yours.  If you see abuse, report it to us using the ‘report post’ button on the forums.
  </div>
</div>

<div class="box">
  <div class="content2">
    <h3>Empowering</h3>
    We empower our members to make decisions that support good mental health and wellbeing.  Don't hold back in sharing your knowledge – it's likely someone will find it useful or interesting. When you give information, provide your sources.
  </div>
</div>

<div class="box">
  <div class="content2">
    <h3>Safe</h3>
    We support our members to talk openly about difficult subjects in a safe manner.  Remember that the forums are public. Don't post personal information that you would not be comfortable sharing with a stranger. We recommend you use only your first name or a pseudonym in your username and that you don't post any information that may identify you or anyone else, such as your address, email address or phone number.
  </div>
</div>

<div class="box">
  <div class="content2">
    <h3>Friendly</h3>
    We approach all our discussions with kindness, warmth and always assume the best intentions.  Welcome new members, share your tips or show them how to use the website. If you are a new member, make sure to introduce yourself – our community is waiting for you.<br />
    <br />
    <strong>It will also be helpful if you:</strong><br />
    Stay on topic. When creating a new discussion thread, give a clear topic title and put your post in the appropriate category. When contributing to an existing discussion, try to stay 'on topic'. If something new comes up within a topic that you would like to discuss, start a new thread.
    One account per person. Running multiple identities on our forums is not permitted. If you are unable to access your account, please get in touch with us rather than creating a new one.
  </div>
</div>

<div class="box">
  <div class="content2">
    <h3>We maintain the right to remove posts and threads</h3>
    We need to make sure that material posted in the discussion forums is not potentially harmful. For this reason, we may edit or choose not to publish any post, avatar or display name that:<br />
    <br />
    - contains disrespectful or derogatory remarks about any other member<br />
    - contains advice or content that we believe is damaging, unhelpful or distressing to others<br />
    - contains links<br />
    - contains swearing or offensive language is nonsensical and/or irrelevant<br />
    - promotes personal beliefs in a way that is disrespectful of the choices of others<br />
    - infringes the privacy of individuals or service providers<br />
    - is racist, sexist, homophobic, sexually explicit or suggestive, abusive or otherwise discriminatory or objectionable<br />
    - advertises products, services, events or research<br />
    - makes any reference to specific prescription medication names and/or dosages or seeks medical advice<br />
    - includes personal information such as images clearly displaying your face, full names, phone numbers, locations, postal or email addresses; or encourages the sharing of such details<br />
    - puts overt pressure on other members to respond, including 'goodbye' messages<br />
    - makes attempt to facilitate personal offline contact with other members<br />
    - describes or encourages violence, suicide or other activity which could endanger the safety or wellbeing of others<br />
    - contains methods or detail of suicide, self-harm or sexual abuse<br />
    - is a copy of another post or contains the same, or similar, message posted multiple times elsewhere<br />
    - contains references to edits or moderation<br />
    - is more than 2,500 characters in length or submitted in multiple parts to avoid the character limit<br />
    - is made from duplicate accounts.
  </div>
</div>

<div class="box">
  <div class="content2">
    <img src="/public/images/tibia/magenews_250.png" style="float:right;" />
    <h3>Moderation</h3>
    <strong>A team of moderators have been entrusted with the ability to intervene when these Community Rules have been breached. However, due to the dynamic nature and the sheer volume of posts, we can't immediately read everything written – therefore much of the responsibility for maintaining our friendly environment lies with you.</strong><br />
    <br />
    If any material you post raises concerns about your safety or the safety of others, we may try to contact you to make sure that you or others are safe. We may also need to pass your contact details on to authorities who can help protect your safety or the safety of others (for example, to the police or a mental health crisis service).<br />
    <br />
    Should you wish to query a moderation decision, please get in touch with us privately via email and we will respond on the next business day.<br />
    <br />
    You will be advised by email if we are unable to publish your post, or if a post has to be edited or removed from the forums because it breaches our community rules. We take our duty of care in providing a safe environment for all forum members seriously, and this includes recognising when a member may benefit from participating in our forums and when it may be more appropriate to seek support offline.<br />
    <br />
    We reserve the right to permanently deactivate the accounts of users who breach our community rules.<br />
    <br />
    Threads from users who are no longer on the forums may be archived (locked or unpublished) at the discretion of moderators.<br />
  </div>
</div>

<div class="box">
  <div class="content2">
    <h3>International users</h3>
    As Beyond Blue is primarily funded by Australian Governments (both National and State/Territory) and members of the Australian public, we need to ensure that our service is accessible and available primarily within Australia.<br />
    <br />
    We encourage users outside of Australia to access services in the country where they are, or to utilise international services, of which there are a number that can be found via Google.<br />
    <br />
    Accounts that have been created using a false postcode and later found to be breaching this rule will be deactivated.<br />
    <br />
    <strong>Ending your time with us</strong><br />
    You may request at any time that your membership be cancelled from the Website.<br />
    <br />
    By doing so, you understand and agree that we may retain your personal information for up to six months from the date of your request and that all activity associated with your account that was generated prior to the date of your cancellation request will remain permanently on the Website (for example, your posts in the forums will not be deleted).<br />
  </div>
</div>